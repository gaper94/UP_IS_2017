#include <iostream>

using namespace std;

int main()
{
    cout << "Size of bool is " << sizeof(bool) << endl;
    cout << "Size of char is " << sizeof(char) << endl;
    cout << "Size of unsigned char is " << sizeof(unsigned char) << endl;
    cout << "Size of short is " << sizeof(short) << endl;
    cout << "Size of unsigned short is " << sizeof(unsigned short) << endl;
    cout << "Size of int is " << sizeof(int) << endl;
    cout << "Size of unsigned int is " << sizeof(unsigned int) << endl;
    cout << "Size of float is " << sizeof(float) << endl;
    cout << "Size of long long int is " << sizeof(long long int) << endl;
    cout << "Size of unsigned long long int is " << sizeof(unsigned long long int) << endl;
    cout << "Size of double is " << sizeof(double) << endl;
    return 0;
}